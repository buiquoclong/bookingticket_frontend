import React, { useRef } from "react";
import {
  Link as MuiLink,
  FormControl,
  InputLabel,
  Select,
  MenuItem,
  TextField,
} from "@mui/material";
import "./TableComponent.scss";

const GenericAdminHeader = ({
  title = "Admin Page",
  breadcrumbLinks = [],
  searchValue,
  setSearchValue,
  searchOptions = [],
  searchCriteria,
  handleCriteriaChange,
  addButtonLabel = "Th√™m",
  onAddClick,
}) => {
  const dateInputRef = useRef();

  // üîπ T√¨m option hi·ªán t·∫°i d·ª±a tr√™n searchCriteria
  const selectedOption = searchOptions.find(
    (opt) => opt.key === searchCriteria
  );
  const optionLabel = selectedOption ? selectedOption.label : "";
  const fieldType = selectedOption ? selectedOption.type : "text";

  return (
    <div className="generic-admin-header">
      {/* Breadcrumb */}
      <div className="breadcrumbs">
        {breadcrumbLinks.map((link, index) => (
          <React.Fragment key={index}>
            <MuiLink underline="hover" color="inherit" href={link.href}>
              {link.label}
            </MuiLink>
            {index < breadcrumbLinks.length - 1 && (
              <span className="separator">/</span>
            )}
          </React.Fragment>
        ))}
      </div>

      <div className="header-main">
        <div className="title">{title}</div>

        <div className="actions">
          {searchOptions.length > 0 && (
            <div className="search-group">
              {/* üî∏ Hi·ªÉn th·ªã input ph√π h·ª£p theo type */}
              {/* üî∏ Hi·ªÉn th·ªã input ph√π h·ª£p theo type */}
              {fieldType === "select" ? (
                <FormControl
                  size="small"
                  sx={{
                    minWidth: "180px",
                    "& .MuiOutlinedInput-root": {
                      borderRadius: "8px",
                      height: "40px",
                    },
                  }}
                >
                  <InputLabel id="select-label">{`Ch·ªçn ${optionLabel}`}</InputLabel>
                  <Select
                    labelId="select-label"
                    label={`Ch·ªçn ${optionLabel}`}
                    value={searchValue || ""}
                    onChange={(e) => setSearchValue(e.target.value)}
                  >
                    <MenuItem value="">
                      <em>-- Ch·ªçn {optionLabel} --</em>
                    </MenuItem>
                    {Array.isArray(selectedOption.options)
                      ? selectedOption.options.map((opt, i) => (
                          <MenuItem key={i} value={opt.value || opt.id}>
                            {opt.label || opt.name}
                          </MenuItem>
                        ))
                      : Object.entries(selectedOption.options || {}).map(
                          ([key, label]) => (
                            <MenuItem key={key} value={key}>
                              {label}
                            </MenuItem>
                          )
                        )}
                  </Select>
                </FormControl>
              ) : fieldType === "date" || fieldType === "datetime" ? (
                <div
                  style={{ display: "inline-block" }}
                  onClick={() => dateInputRef.current?.showPicker?.()}
                >
                  <TextField
                    type={fieldType === "datetime" ? "datetime-local" : "date"}
                    label={optionLabel}
                    variant="outlined"
                    size="small"
                    value={
                      searchValue
                        ? fieldType === "datetime"
                          ? searchValue.replace(" ", "T") // hi·ªÉn th·ªã ƒë√∫ng ƒë·ªãnh d·∫°ng cho datetime-local
                          : searchValue
                        : ""
                    }
                    onChange={(e) => {
                      const val = e.target.value;
                      // N·∫øu l√† datetime-local ‚Üí format l·∫°i gi√° tr·ªã tr∆∞·ªõc khi l∆∞u
                      if (fieldType === "datetime") {
                        const formatted = val.replace("T", " ");
                        setSearchValue(formatted);
                      } else {
                        setSearchValue(val);
                      }
                    }}
                    inputRef={dateInputRef}
                    InputLabelProps={{ shrink: true }}
                    sx={{
                      "& .MuiOutlinedInput-root": {
                        borderRadius: "8px",
                        height: "40px",
                      },
                      "& .MuiInputLabel-root": {
                        fontSize: "0.9rem",
                        top: "2px",
                      },
                      "& input": { padding: "10px 12px", cursor: "pointer" },
                    }}
                  />
                </div>
              ) : (
                <TextField
                  type="text"
                  label={`T√¨m ki·∫øm b·∫±ng ${optionLabel}`}
                  variant="outlined"
                  size="small"
                  value={searchValue}
                  onChange={(e) => setSearchValue(e.target.value)}
                  sx={{
                    "& .MuiOutlinedInput-root": {
                      borderRadius: "8px",
                      height: "40px",
                    },
                    "& .MuiInputLabel-root": { fontSize: "0.9rem", top: "2px" },
                    "& input": { padding: "10px 12px" },
                  }}
                />
              )}

              {/* üîπ B·ªô ch·ªçn ti√™u ch√≠ t√¨m ki·∫øm */}
              <FormControl
                variant="outlined"
                size="small"
                className="search-select"
                sx={{
                  minWidth: "160px",
                  "& .MuiOutlinedInput-root": {
                    borderRadius: "8px",
                    height: "40px",
                  },
                }}
              >
                <InputLabel id="criteria-label">T√¨m ki·∫øm b·∫±ng</InputLabel>
                <Select
                  labelId="criteria-label"
                  value={searchCriteria}
                  onChange={handleCriteriaChange}
                  label="T√¨m ki·∫øm b·∫±ng"
                >
                  {searchOptions.map((option) => (
                    <MenuItem key={option.key} value={option.key}>
                      {option.label}
                    </MenuItem>
                  ))}
                </Select>
              </FormControl>
            </div>
          )}

          {onAddClick && (
            <button className="btn add-btn" onClick={onAddClick}>
              {addButtonLabel}
            </button>
          )}
        </div>
      </div>
    </div>
  );
};

export default GenericAdminHeader;
